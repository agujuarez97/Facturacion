    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        try {
            Conexion sc = new Conexion("org.postgresql.Driver", "jdbc:postgresql://127.0.0.1:5432/postgres", "root", DriverManager.getConnection("jdbc:postgresql://127.0.0.1:5432/postgres", "postgres", "root"), "postgres");
            String query2 = "SELECT login FROM facturacion.conf_usuario WHERE login = '" + login.getText() + "'";
            PreparedStatement statement2 = sc.getConnectio().prepareStatement(query2);
            ResultSet res2 = statement2.executeQuery();
            if (res2.next()) {
                UsuarioExistente acep = new UsuarioExistente();
                acep.setVisible(true);
            } else {
                String query3 = "INSERT INTO facturacion.conf_usuario(login, pas, nombre) VALUES (?,?,?)";
                PreparedStatement statement3 = sc.getConnectio().prepareStatement(query3);
                statement3.setString(1, login.getText());
                statement3.setString(2, password.getText());
                statement3.setString(3, nombre.getText());
                statement3.executeUpdate();
                this.dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registracion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_aceptarActionPerformed

