    private void ModificarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarArticuloActionPerformed
        try {
            int row = TablaArticulo.getSelectedRow();
            if (row != -1) {
                this.dispose();
                EditarArticulo eA = new EditarArticulo();
                eA.setVisible(true);
                String codArticulo = TablaArticulo.getValueAt(row, 0).toString();
                String codBarra = TablaArticulo.getValueAt(row, 1).toString();
                String nombreArticulo = TablaArticulo.getValueAt(row, 2).toString();
                String descArticulo = TablaArticulo.getValueAt(row, 3).toString();
                String stock = TablaArticulo.getValueAt(row, 4).toString();
                String precioFinal = TablaArticulo.getValueAt(row, 5).toString();

                eA.getCodigo().setText(codArticulo);
                eA.getCodigo_barra().setText(codBarra);
                eA.getNombre().setText(nombreArticulo);
                eA.getDescripcion().setText(descArticulo);
                eA.getStock().setText(stock);
                eA.getPrecioF().setText(precioFinal);

                Conexion sc = new Conexion("org.postgresql.Driver", "jdbc:postgresql://127.0.0.1:5432/postgres", "root", DriverManager.getConnection("jdbc:postgresql://127.0.0.1:5432/postgres", "postgres", "root"), "postgres");
                String query7 = "SELECT precio_compra, margen, precio_venta, iva, stock_minimo, stock_maximo, estado FROM facturacion.Articulo WHERE cod_articulo = '" + codArticulo + "'";
                PreparedStatement statement7 = sc.getConnectio().prepareStatement(query7);
                ResultSet res = statement7.executeQuery();
                res.next();

                eA.getPrecioC().setText(res.getString("precio_compra"));
                eA.getMargen().setText(res.getString("margen"));
                eA.getPrecioV().setText(res.getString("precio_venta"));
                eA.getStockMin().setText(res.getString("stock_minimo"));
                eA.getStockMax().setText(res.getString("stock_maximo"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PantallaArticulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ModificarArticuloActionPerformed

