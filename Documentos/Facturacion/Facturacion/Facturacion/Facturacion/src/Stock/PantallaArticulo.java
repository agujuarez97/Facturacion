        ResultSet re = statement6.executeQuery();
        while (re.next()) {
            registros[0] = re.getString("cod_articulo");
            registros[1] = re.getString("EAN");
            registros[2] = re.getString("nombre");
            registros[3] = re.getString("descripsion");
            registros[4] = re.getInt("stock");
            registros[5] = re.getFloat("precio_final");
            ((DefaultTableModel) TablaArticulo.getModel()).addRow(registros);
        }
