    private void Nuevo_pagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Nuevo_pagoActionPerformed

        int row = TablaCuentaCorriente.getSelectedRow();
        if (row != -1) {
            this.dispose();
            NuevoPago np = new NuevoPago();
            np.setVisible(true);
            String codigo = TablaCuentaCorriente.getValueAt(row, 0).toString();
            String razonsocial = TablaCuentaCorriente.getValueAt(row, 1).toString();
            String monto = TablaCuentaCorriente.getValueAt(row, 4).toString();

            np.getCodigo().setText(codigo);
            np.getRazonSocial().setText(razonsocial);
            np.getDeuda().setText(monto);
        }

    }//GEN-LAST:event_Nuevo_pagoActionPerformed

