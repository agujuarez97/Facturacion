        Object[] registros;
        registros = new Object[8];
        String query6 = "select ven_cc.cod_cliente, razon_social, nom_comercial, num_dni, monto_total from facturacion.ven_cc join facturacion.ven_cliente on ven_cliente.cod_cliente = ven_cc.cod_cliente";
